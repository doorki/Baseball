package com.bo.stats.vo;

public class StatsVO {
	private int id;
	private String rank;
	private String team;
	private String round;
	private String win;
	private String lose;
	private String draw;
	private String winAvg;
	private String gamesBehind;
	private String straight;
	private String onBase;
	private String longHit;
	private String recentTenRound;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getRound() {
		return round;
	}
	public void setRound(String round) {
		this.round = round;
	}
	public String getWin() {
		return win;
	}
	public void setWin(String win) {
		this.win = win;
	}
	public String getLose() {
		return lose;
	}
	public void setLose(String lose) {
		this.lose = lose;
	}
	public String getDraw() {
		return draw;
	}
	public void setDraw(String draw) {
		this.draw = draw;
	}
	public String getWinAvg() {
		return winAvg;
	}
	public void setWinAvg(String winAvg) {
		this.winAvg = winAvg;
	}
	public String getGamesBehind() {
		return gamesBehind;
	}
	public void setGamesBehind(String gamesBehind) {
		this.gamesBehind = gamesBehind;
	}
	public String getStraight() {
		return straight;
	}
	public void setStraight(String straight) {
		this.straight = straight;
	}
	public String getOnBase() {
		return onBase;
	}
	public void setOnBase(String onBase) {
		this.onBase = onBase;
	}
	public String getLongHit() {
		return longHit;
	}
	public void setLongHit(String longHit) {
		this.longHit = longHit;
	}
	public String getRecentTenRound() {
		return recentTenRound;
	}
	public void setRecentTenRound(String recentTenRound) {
		this.recentTenRound = recentTenRound;
	}
	
	
}
